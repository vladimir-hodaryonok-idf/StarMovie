class MovieDetails {
  final String title;
  final String overview;
  final String durationAndCertification;
  final String genres;
  final String image;
  final int stars;
  final String rating;
  final int id;

  const MovieDetails(
    this.title,
    this.overview,
    this.durationAndCertification,
    this.genres,
    this.image,
    this.stars,
    this.rating,
    this.id,
  );
}
